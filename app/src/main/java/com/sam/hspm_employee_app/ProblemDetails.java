package com.sam.hspm_employee_app;

public class ProblemDetails {

    private String PcType;
    private String ProblemType;
    private String SpecifiedProblem;

    public String getPcType() {
        return PcType;
    }

    public void setPcType(String pcType) {
        PcType = pcType;
    }

    public String getProblemType() {
        return ProblemType;
    }

    public void setProblemType(String problemType) {
        ProblemType = problemType;
    }

    public String getSpecifiedProblem() {
        return SpecifiedProblem;
    }

    public void setSpecifiedProblem(String specifiedProblem) {
        SpecifiedProblem = specifiedProblem;
    }
}
